//! MCP tool implementations.
//!
//! Each tool function corresponds to one entry in the MCP `tools/list` response
//! and is dispatched by [`ImapMcpServer`]. The split is by lifecycle:
//! - [`read`] — non-mutating: list, get, search
//! - [`write`](mod@write) — mutating: flag, mark read, move, delete
//! - [`draft`] — message composition + APPEND to Drafts folder
//!
//! Every tool function returns a JSON string (success or `{"error": ...}`).
//! The wrapping into MCP responses happens in [`ImapMcpServer`].

pub mod draft;
pub mod read;
pub mod write;

use std::collections::HashMap;
use std::sync::Arc;
use tokio::sync::Mutex;

use rmcp::{
    ServerHandler,
    handler::server::router::tool::ToolRouter,
    model::{ServerCapabilities, ServerInfo},
    schemars, tool, tool_handler, tool_router,
};

use crate::config::{AccountConfig, ServerConfig};
use crate::imap_client::ImapClient;

pub fn error_json(msg: &str) -> String {
    // Strip async-imap's verbose `Option`-debug framing (turns
    // `no response: code: None, info: Some("[NONEXISTENT] ...")`
    // into `Unknown Mailbox: ...`) and scrub any bidi/control chars a
    // hostile server might have smuggled into the response text.
    // No-op for messages that already match our own error format.
    let cleaned = crate::imap_client::clean_imap_error(msg);
    let safe = crate::email::sanitize_external_str(&cleaned);
    serde_json::to_string(&serde_json::json!({"error": safe})).unwrap()
}

#[derive(Debug, Clone)]
pub struct ImapMcpServer {
    pub config: ServerConfig,
    pub clients: HashMap<String, Arc<Mutex<ImapClient>>>,
    // Populated by `Self::tool_router()` (generated by `#[tool_router]`) and
    // consumed by the `#[tool_handler]` macro-expanded `ServerHandler` impl.
    // The consumer is macro-generated, so Clippy's dead-code analyzer can't
    // see the read site after the rmcp 1.5 macro refactor.
    #[allow(dead_code)]
    tool_router: ToolRouter<Self>,
}

impl ImapMcpServer {
    pub fn new(config: ServerConfig, clients: HashMap<String, Arc<Mutex<ImapClient>>>) -> Self {
        Self {
            config,
            clients,
            tool_router: Self::tool_router(),
        }
    }

    /// Resolve an account name to its config and client.
    /// Case-insensitive matching. Uses first account if name is None.
    pub fn resolve_client(
        &self,
        account: Option<&str>,
    ) -> Result<(&AccountConfig, Arc<Mutex<ImapClient>>), String> {
        // Must use Unicode-aware `to_lowercase()` (not `eq_ignore_ascii_case`)
        // so accounts named with uppercase non-ASCII chars (e.g. "BÜRO")
        // stay resolvable after the tool-layer lookup lowercases the input.
        // The few per-call allocations here are cheaper than silently
        // breaking a user's config.
        let lookup_name = account.map_or_else(
            || {
                self.config
                    .accounts
                    .first()
                    .map(|a| a.name.to_lowercase())
                    .unwrap_or_default()
            },
            str::to_lowercase,
        );

        let account_config = self
            .config
            .accounts
            .iter()
            .find(|a| a.name.to_lowercase() == lookup_name)
            .ok_or_else(|| format!("Account \"{lookup_name}\" not found"))?;

        let client = self
            .clients
            .get(&lookup_name)
            .ok_or_else(|| format!("No client for account \"{lookup_name}\""))?
            .clone();

        Ok((account_config, client))
    }
}

#[derive(Debug, serde::Deserialize, schemars::JsonSchema)]
pub struct ListAccountsRequest {}

